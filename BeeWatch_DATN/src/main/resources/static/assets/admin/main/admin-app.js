app = angular.module("admin-app",["ngRoute"]);
app.config(function ($routeProvider){
	$routeProvider
	.when("/category",{
		templateUrl:"/assets/admin/category/index_category.html",
		controller:"category-ctrl"
	})
	.when("/product",{
		templateUrl:"/assets/admin/product/index.html",
		controller:"product-ctrl"
	})
	.when("/feedback",{
		templateUrl:"/assets/admin/feedback/Feedback.html",
		controller:"feedback-ctrl"
	})
	.when("/brand",{
		templateUrl:"/assets/admin/brand/index_brand.html",
		controller:"brand-ctrl"
	})
	.when("/voucher",{
		templateUrl:"/assets/admin/voucher/Voucher_sua.html",
		controller:"voucher-ctrl"
	})
	.when("/customer",{
		templateUrl:"/assets/admin/customer/index_customer.html",
		controller:"customer-ctrl"
	})
	.when("/account",{
		templateUrl:"/assets/admin/account/QLYttTaiKhoan.html",
		controller:"account-ctrl"
	})
	.when("/news",{
		templateUrl:"/assets/admin/news/Index_news.html",
		controller:"news-ctrl2"
	})
	.when("/discount",{
		templateUrl:"/assets/admin/discount/index_discount.html",
		controller:"discount-ctrl"
	})
	.when("/thongke",{
		templateUrl:"/assets/admin/thongKe/thongke.html",
		controller:"thongke-ctrl"
	})
	.when("/hoadon",{
		templateUrl:"/assets/admin/hoadon/HoaDon.html",
		controller:"order-ctrl"
	})
	.when("/size",{
		templateUrl:"/assets/admin/size/index_size.html",
		controller:"size-ctrl"
	})
	.when("/strap",{
		templateUrl:"/assets/admin/strap/index_strap.html",
		controller:"strap-ctrl"
	})
	.when("/glass",{
		templateUrl:"/assets/admin/glass/index_glass.html",
		controller:"glass-ctrl"
	})
	.when("/water",{
		templateUrl:"/assets/admin/water/index_water.html",
		controller:"water-ctrl"
	})
	.when("/shell",{
		templateUrl:"/assets/admin/shell/index_shell.html",
		controller:"shell-ctrl"
	})
	.otherwise({
		templateUrl:"/assets/admin/thongKe/thongke.html",
		controller:"thongke-ctrl"
	})
	
})